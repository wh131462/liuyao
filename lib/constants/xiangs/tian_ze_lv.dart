上九：行为审慎，遇事周密反复地考虑，大吉。
《象辞》说：大吉大利，因为上九之爻居全卦之首。预兆其人将有重大喜庆之事。
北宋易学家邵雍解
吉：得此爻者，财利可获。做官的宜退而以享安静和平之福。读书人努力者可获佳绩。
台湾国学大儒傅佩荣解
时运：晚运亨通，福寿双全。
财运：往来经营，无不有利。
家宅：积善之家才有余庆。
身体：天年有限。
