《象》曰：“屯其膏”，施未光也。
这里指出：“只顾自己囤积财富而不注意帮助别人”，这样的人即使想有所作为，其前景也不大光明。
若是普通人占得此卦，你要懂得取舍之道。只顾自己囤积财富而不注意帮助别人，是很危险的，那样做，办小事虽有成功的可能，但办大事则必然会出现凶险。所以，不要光盯着自己的钱口袋，要“广施恩泽”，懂得舍，才能避凶趋吉。
